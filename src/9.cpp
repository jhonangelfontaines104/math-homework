#include <iostream>
#include <cmath>

int main() {
  double a = 2.5;
  double b = 3.7;
  double c = 1.9;
  double d = 4.2;

  std::cout << "a + b = " << a + b << std::endl;
  std::cout << "c - d = " << c - d << std::endl;
  std::cout << "a * b = " << a * b << std::endl;
  std::cout << "c / d = " << c / d << std::endl;
  std::cout << "sqrt(d) = " << sqrt(d) << std::endl;
}
